package com.epam.module1.taskif4;

public class Rectangle {

	double A;
	double B;

	public Rectangle(double a, double b) {
		A = a;
		B = b;
	}

	public double getA() {
		return A;
	}

	public void setA(double a) {
		A = a;
	}

	public double getB() {
		return B;
	}

	public void setB(double b) {
		B = b;
	}
}
